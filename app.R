#Callback for Plot1
app$callback(
    output('plot-area', 'figure'),
    list(input('region_selector', 'value'),
         input('platform_selector', 'value'),
         input('genre_selector', 'value'),
         input('publisher_selector', 'value'),
         input('year_selector', 'value')),
    function(reg,plat,gen,pub,years) {
        # Input: List of Regions, Platforms, Genres, Publishers, Min and Max Year
        # Output: Graph
        #
        # Create subset based on filters 
        # Pass to graph
        # Output graph
        if ("Global_Sales" %in% reg){
            region_filter = list("Global_Sales")
        } else {
            region_filter = reg
        }
        if ("all" %in% plat){
            platform_filter = unique(game_melt$Platform)
        } else {
            platform_filter = plat
        }
        if ("all" %in% gen){
            genre_filter = unique(game_melt$Genre)
        } else {
            genre_filter = gen
        }
        if ("all" %in% pub){
            publisher_filter = unique(game_melt$Publisher)
        } else {
            publisher_filter = pub
        }
        min_year = years[1]
        max_year = years[2]
        
        graph1 <- game_melt[,3:8] %>% 
            subset(Region %in% region_filter & Platform %in% platform_filter & Genre %in% genre_filter & Publisher %in% publisher_filter & Year >= min_year & Year <= max_year) %>%
            group_by(Year,Genre) %>%
            summarise("Copies Sold" = sum(`Sales`)) %>% 
            ggplot() +
            aes(x=as.factor(Year),
                y=`Copies Sold`,
                fill = Genre) + 
            geom_bar(stat="identity")+
            theme(axis.text.x = element_text(angle = 90, hjust=0.95, vjust=0.2)) +
            ylab("Number of Copies Sold (in millions)")+
            xlab("Year")
        
        return (ggplotly(graph1))
    }
)

